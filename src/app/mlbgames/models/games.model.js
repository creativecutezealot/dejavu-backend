        AwayTeam: { type: String, default: null },
        HomeTeam: { type: String, default: null },
        AwayTeamID: { type: Number, default: null },
        HomeTeamID: { type: Number, default: null },
        RescheduledGameID: { type: Number, default: null },
        StadiumID: { type: Number, default: null },
