        HomeTeamHits: { type: Number, default: null },
        AwayTeamErrors: { type: Number, default: null },
        HomeTeamErrors: { type: Number, default: null },
        WinningPitcherID: { type: Number, default: null },
        LosingPitcherID: { type: Number, default: null },
        SavingPitcherID: { type: Number, default: null },
        Attendance: { type: Number, default: null },
        AwayTeamProbablePitcherID: { type: Number, default: null },
        HomeTeamProbablePitcherID: { type: Number, default: null },
