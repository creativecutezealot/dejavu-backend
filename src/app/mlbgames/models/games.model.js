        AwayTeamMoneyLine: { type: Number, default: null },
        HomeTeamMoneyLine: { type: Number, default: null },
        ForecastTempLow: { type: Number, default: null },
        ForecastTempHigh: { type: Number, default: null },
        ForecastDescription: { type: String, default: null },
        ForecastWindChill: { type: Number, default: null },
